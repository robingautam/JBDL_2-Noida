package org.gfg;

import java.util.List;

public class Parent {
    int parentField;
    List<String> fruits;

    public int getParentField() {
        return parentField;
    }

    public void setParentField(int parentField) {
        this.parentField = parentField;
    }

    public List<String> getFruits() {
        return fruits;
    }

    public void setFruits(List<String> fruits) {
        this.fruits = fruits;
    }
}
